				<br>
				<span>There are plenty of ways of cataloging your personal collection, this is one of those ways that I personally use. You can feel free to use any way you would like, though!</span>
				<br>
				<br>
				<span>Let's take a quick look at "The Record Screen", A.K.A. the screen you will be seeing the most when using the program. We'll break this screen down more when we get to the next page of the guide, so don't worry about being overwhelmed with information!</span>
				<br>
				<br>
				<span><a href="assets/testrecord.png" target="_blank"><img class="manualimg" src="assets/testrecord.png" alt="Test Record Screen" title="Test Record Screen"></img></a></span>
				<br>
				<span class="smallnostar">By the way, you can always click on an image to make it bigger.</span>
				<br>
				<br>
				<span>Right off the bat, you'll notice a few different pieces of information. Let's focus on the top left for this portion of the guide, specifically, this line:</span>
				<br>
				<br>
				<span><a href="assets/dataline.png" target="_blank"><img class="manualimg" src="assets/dataline.png" alt="Data Line" title="Data Line"></img></a></span>
				<br>
				<br>
				<span>This is referred to as the "Data Line", where 2 pieces of information live: the barcode and line number. The barcode is used throughout CCIDB to make sure you have the right entry (specifically, the barcode uses the <a href="https://en.wikipedia.org/wiki/Universal_Product_Code" target="_blank">UPC standard</a>), whereas the line number is a more "user friendly" way of just looking up a given coin in the database. </span>
				<br>
				<br>
				<span>Now, let's say you want to catalog your collection using the barcode number. That's the recommended way of doing things, as it allows you to print out actual, scannable barcodes, that when combined with a barcode scanner from the likes of Amazon, ultimately makes you more organized. In this example, we will use a label maker with the ability to make barcodes. There are a lot of options in this category, but the one I personally use is a <a href="https://www.amazon.com/Brother-PC-Connectable-PTD600-High-Resolution-Printing/dp/B00OCEKCB2" target="_blank">Brother PT-D600</a>. If you want to go with a cheaper option, check out the <a href="https://www.amazon.com/Machine-D30-Portable-Bluetooth-Memoking-Compatible/dp/B08JQ6ZC8W/" target="_blank">Memoking D30</a>. Label maker in hand, let's make a label!</span>
				<br>
				<br>
				<span>Instuctions will differ depending on the label maker brand and model, but using the Brother PT-D600, it's as simple as:</span>
				<br>
				<br>
				<span><a href="assets/barcode_1.png" target="_blank"><img class="manualimg" src="assets/barcode_1.png" alt="Label Maker" title="Label Maker"></img></a></span>
				<br>
				<br>
				<span>1. Turn the label maker on and select "Create Label" using the "OK" key.</span>
				<br>
				<br>
				<span><a href="assets/barcode_2.png" target="_blank"><img class="manualimg" src="assets/barcode_2.png" alt="Label Maker" title="Label Maker"></img></a></span>
				<br>
				<br>
				<span>2. Press the "Fn" Key, press the up arrow key a few times, and you should see an option for "Bar Code". Press "OK".</span>
				<br>
				<br>
				<span><a href="assets/barcode_3.png" target="_blank"><img class="manualimg" src="assets/barcode_3.png" alt="Label Maker" title="Label Maker"></img></a></span>
				<br>
				<br>
				<span>3. Here you can customize the look of the barcode. With the default settings, it will print a small barcode with the number underneath the barcode. Change the "Width" and "Under#" settings if you wish and press "OK".</span>
				<br>
				<br>
				<span><a href="assets/barcode_4.png" target="_blank"><img class="manualimg" src="assets/barcode_4.png" alt="Label Maker" title="Label Maker"></img></a></span>
				<br>
				<br>
				<span>4. Enter the barcode number from CCIDB. In this example, enter "5241608477". The last digit (6) will automatically be filled in for you. Press "OK".</span>
				<br>
				<br>
				<span><a href="assets/barcode_5.png" target="_blank"><img class="manualimg" src="assets/barcode_5.png" alt="Label Maker" title="Label Maker"></img></a></span>
				<br>
				<br>
				<span>5. Hit "Esc", then "Print", then "Print" yet again. The barcode will be printed on the left hand side of the machine.</span>
				<br>
				<br>
				<span><a href="assets/barcode_6.png" target="_blank"><img class="manualimg" src="assets/barcode_6.png" alt="Label Maker" title="Label Maker"></img></a></span>
				<br>
				<br>
				<span>6. You're done! You have successfully printed a barcode for attaching to your 2x2 or slab or anything else!</span>
				<br>
				<br>
				<span>Now it's time to take your newly printed barcode and put it on the corresponding item. In this example, I have a small 2x2 with a penny inside it.</span>
				<br>
				<br>
				<span><a href="assets/penny.png" target="_blank"><img class="manualimg" src="assets/penny.png" alt="Example Penny" title="Example Penny"></img></a></span>
				<br>
				<br>
				<span>And now, you have cataloged your first coin! Rinse and repeat as many times as you have coins, and you're good to go! If your collection is especially large, this process might take a while, but will be well worth it in the end.</span>
